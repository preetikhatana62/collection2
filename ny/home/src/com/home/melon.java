package com.home;



public class melon {
	
	private  String type = "";
	private int weight = 0;

	

	
		
	



	public melon(String type, int weight) {
		this.type=type;
		this.weight=weight;
	}



	public String getType() {
		return type;
	}



	public void setType(String type) {
		this.type = type;
	}



	public int getWeight() {
		return weight;
	}



	public void setWeight(int weight) {
		this.weight = weight;
	}
}
	



	